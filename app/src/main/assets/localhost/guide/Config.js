function getTime(ms){
		if (null!=ms)
			var today=new Date(ms);
		else
			var today=new Date();

		msOffest=today.getTime()-today.getTimezoneOffset() *60*1000;

		var jsonTime;

		var ampm="";
		var hours=today.getHours();
		if (hours>12 && TWELVEHOUR)	{
			hours-=12;
			ampm="p"
		}else if (hours>11 && TWELVEHOUR){
			ampm="p"
		}else if (hours==0 && TWELVEHOUR){
			hours=12;
			ampm="a";
		}else if(TWELVEHOUR){
			ampm="a";
		}
		var displayTime=hours+":";
		var displayMins=today.getMinutes()<10?("0"+today.getMinutes()):(""+today.getMinutes());
		var duration=displayTime + displayMins;
		var displayTime=displayTime + displayMins + ampm;

		var todayTZOffest=new Date(msOffest);
		var todayUTCstring = todayTZOffest.toUTCString();
		var displayTodayArray=todayUTCstring.split(' ');
		var displayDate=displayTodayArray[0]+' '+displayTodayArray[1]+' '+displayTodayArray[2]+' '+displayTodayArray[3];

		jsonTime={'ms':today.getTime(),"date":displayDate, "time": displayTime, "all":(displayDate+ ' '+displayTime), "duration":duration};
		return jsonTime;

}

